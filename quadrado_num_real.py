"""
Leia um número real e imprima o resultado do quadrado desse número
"""
num = int(input('Digte um número real para ver o seu quadrado: '))
num2 = int(input('Digite outro número: '))
soma = (num ** num2)
print(num)
print(num2)
print('O quadrado do número um é {} e o quadrado do segundo é {}'.format(num, num2, soma))
print(soma)
